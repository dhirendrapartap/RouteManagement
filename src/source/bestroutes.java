    public bestroutes() {
        initComponents();
        GroceryKartSavedData gs = new GroceryKartSavedData();
        gs.readFromFile();
        Candidate can = gs.tmpCandidates.get(0);

        Route bestFitnessRoute = can.routeTable.get(0);
        double bestFitness = bestFitnessRoute.routeFitness;
        for (int i = 1; i < can.routeTable.size(); i++) {
            if (can.routeTable.get(i).routeFitness < bestFitness) {
                bestFitnessRoute = can.routeTable.get(i);
                bestFitness = bestFitnessRoute.routeFitness;
            }
        }
        String bestFitnessValue = "" + bestFitnessRoute.route.get(0);
        for (int i = 1; i < bestFitnessRoute.route.size(); i++) {
            bestFitnessValue += "->" + bestFitnessRoute.route.get(i);
        }

        Route bestDemandRoute = can.routeTable.get(0);
        int bestDemand = bestDemandRoute.route.size();
        for (int i = 1; i < can.routeTable.size(); i++) {
            if (can.routeTable.get(i).route.size() > bestDemand) {
                bestDemandRoute = can.routeTable.get(i);
                bestDemand = bestDemandRoute.route.size();
            }
        }
        String bestDemandValue = "" + bestDemandRoute.route.get(0);
        for (int i = 1; i < bestDemandRoute.route.size(); i++) {
            bestDemandValue += "->" + bestDemandRoute.route.get(i);
        }

        jLabel3.setText(bestFitnessValue);
        jLabel4.setText(bestDemandValue);

        String allroutes = "";
        for (Route r : can.routeTable) {
            String tmp = "" + r.route.get(0);
            for (int i = 1; i < r.route.size(); i++) {
                tmp += "->" + r.route.get(i);
            }
            allroutes += tmp;
            allroutes += "\n";
        }
        
        jTextArea1.setText(allroutes);
        
    }

